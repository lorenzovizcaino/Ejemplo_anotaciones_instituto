package util;

public class ProfeInfo {

	private String nombreCompleto;

	
	
	public ProfeInfo(String nombreCompleto) {
		super();
		this.nombreCompleto = nombreCompleto;
	
	}
	public String getNombreCompleto() {
		return nombreCompleto;
	}
	public void setNombreCompleto(String nombreProfe) {
		this.nombreCompleto = nombreProfe;
	}
	
	
	
}